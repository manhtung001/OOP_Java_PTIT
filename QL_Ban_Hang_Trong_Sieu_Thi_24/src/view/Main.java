    private void jComboBox4ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox4ItemStateChanged
       if(jComboBox4.getSelectedIndex()==1){
            lql.sort(new Comparator<BangKe>() {
                @Override
                public int compare(BangKe o1, BangKe o2) {
                    String[] ten1=o1.getKh().getHoten().split("\\s+");
                    String[] ten2=o2.getKh().getHoten().split("\\s+");
                    if(ten1[ten1.length-1].equalsIgnoreCase(ten2[ten2.length-1])) {
                        return o1.getKh().getHoten().compareToIgnoreCase(o2.getKh().getHoten());
                    } else 
                        return ten1[ten1.length-1].compareToIgnoreCase(ten2[ten2.length-1]);
                }
            });
        }
        if(jComboBox4.getSelectedIndex()==2){
            lql.sort(new Comparator<BangKe>() {
                @Override
                public int compare(BangKe o1, BangKe o2) {
                    return o1.getMh().getNhom().compareToIgnoreCase(o2.getMh().getNhom());
                }
            });
        }
        
        tmql.setRowCount(0);
        for(BangKe i:lql)
            tmql.addRow(i.toObject());
    }//GEN-LAST:event_jComboBox4ItemStateChanged

