    private boolean trung2Ma(int maKhach, int maMatHang) {
        for(BangKe i:lql)
            if(i.getKh().getMa()==maKhach && i.getMh().getMa()==maMatHang)
                return true;
        return false;
    }
    
    private void btThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThem2ActionPerformed
        try {
            int maKhach=Integer.parseInt(jComboBox2.getSelectedItem().toString());
            int maMatHang=Integer.parseInt(jComboBox3.getSelectedItem().toString());
            if(trung2Ma(maKhach, maMatHang)) {
                JOptionPane.showMessageDialog(this, "Trung 2 ma!");
                jComboBox2.requestFocus();
                return;
            }
            int soluong=Integer.parseInt(jTextField14.getText());
            
            if(soluong > 5 || soluong <= 0) {
                JOptionPane.showMessageDialog(this, "so luong phai tu 1-5");
                return;
            }
            KhachHang khach=getKhachbyMa(maKhach);
            MatHang matHang=getMatHangbyMa(maMatHang);
            if(khach==null || matHang==null) {
                JOptionPane.showMessageDialog(this, "khong ton tai 1 trong 2 ma");
                return;
            }
           
            BangKe ql = new BangKe(khach, matHang, soluong);
            tmql.addRow(ql.toObject());
            lql.add(ql);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "so luong la so");
        }
    }//GEN-LAST:event_btThem2ActionPerformed

