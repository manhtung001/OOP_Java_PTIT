    private void load_1() {
        File f=new File(f1);
        if(f.exists()) 
            L1=IOFile.doc(f1);
        else 
            L1=new ArrayList<>();
    }
    
    private void load_2() {
        File f=new File(f2);
        if(f.exists()) 
            L2=IOFile.doc(f2);
        else 
            L2=new ArrayList<>();
    }
    
    private void load2Ma() {
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        
        for(KhachHang i:L1)
            jComboBox2.addItem(i.getMa()+"");
        
        for(MatHang i:L2)
            jComboBox3.addItem(i.getMa()+"");           
    }
    
    private void loadQL() {
        File f=new File(fql);
        if(f.exists()) 
            lql=IOFile.doc(fql);
        else 
            lql=new ArrayList<>();
    }
    
    private void show_1(List<KhachHang> list) {
        tm1.setRowCount(0);
        for(KhachHang i:list)
            tm1.addRow(i.toObject());
    }
    
    private void show_2(List<MatHang> list) {
        tm2.setRowCount(0);
        for(MatHang i:list)
            tm2.addRow(i.toObject());
    }
    
    private void showQL(List<BangKe> list) {
        tmql.setRowCount(0);
        for(BangKe i:list)
            tmql.addRow(i.toObject());
    }        

