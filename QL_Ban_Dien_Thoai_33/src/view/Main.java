    private boolean trung2Ma(int maKhach, int maDt) {
        for(HoaDon i:lql)
            if(i.getKh().getMa()==maKhach && i.getDt().getMa()==maDt)
                return true;
        return false;
    }
    
    private void btThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThem2ActionPerformed
        try {
            int maKhach=Integer.parseInt(jComboBox3.getSelectedItem().toString());
            int maDt=Integer.parseInt(jComboBox4.getSelectedItem().toString());
            if(trung2Ma(maKhach, maDt)) {
                JOptionPane.showMessageDialog(this, "Trung 2 ma!");
                jComboBox2.requestFocus();
                return;
            }
            int soluong=Integer.parseInt(jTextField11.getText());
           
            KhachHang khach=getKhachHangbyMa(maKhach);
            DienThoai dt=getDienThoaibyMa(maDt);
            if(khach==null || dt==null) {
                JOptionPane.showMessageDialog(this, "khong ton tai 1 trong 2 ma");
                return;
            }
            HoaDon ql = new HoaDon(khach, dt, soluong);
            tmql.addRow(ql.toObject());
            lql.add(ql);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "so luong la so");
        }
    }//GEN-LAST:event_btThem2ActionPerformed

