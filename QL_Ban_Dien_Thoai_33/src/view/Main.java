    private void jComboBox5ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox5ItemStateChanged
         if(jComboBox5.getSelectedIndex()==1){
            lql.sort(new Comparator<HoaDon>() {
                @Override
                public int compare(HoaDon o1, HoaDon o2) {
                    String[] ten1=o1.getKh().getHoten().split("\\s+");
                    String[] ten2=o2.getKh().getHoten().split("\\s+");
                    if(ten1[ten1.length-1].equalsIgnoreCase(ten2[ten2.length-1])) {
                        return o1.getKh().getHoten().compareToIgnoreCase(o2.getKh().getHoten());
                    } else 
                        return ten1[ten1.length-1].compareToIgnoreCase(ten2[ten2.length-1]);
                }
            });
        }
        if(jComboBox5.getSelectedIndex()==2){
            lql.sort(new Comparator<HoaDon>() {
                @Override
                public int compare(HoaDon o1, HoaDon o2) {
                    return Integer.compare(o1.getSoluong(), o2.getSoluong());
                }
            });
        }
        
        tmql.setRowCount(0);
        for(HoaDon i:lql)
            tmql.addRow(i.toObject());
    }//GEN-LAST:event_jComboBox5ItemStateChanged

