    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        int r=jTable2.getSelectedRow();
        if(r>=0&&r<jTable2.getRowCount()) {
            String ten=jTextField7.getText();
            String mota=jTextField8.getText();
            Double heso=Double.parseDouble(jTextField9.getText());
            tmpb.setValueAt(ten, r, 1);
            tmpb.setValueAt(mota, r, 2);
            tmpb.setValueAt(heso, r, 3);
            int ma = Integer.parseInt(jTextField6.getText());
            PhongBan pb = new PhongBan(ma, ten, mota, heso);
            lpb.set(r, pb);
        }
    }//GEN-LAST:event_btSuaActionPerformed

