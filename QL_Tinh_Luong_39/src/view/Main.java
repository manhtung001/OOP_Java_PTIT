    private void loadNV() {
        File f=new File(fnv);
        if(f.exists())
            lnv=IOFile.doc(fnv);
        else
            lnv=new ArrayList<>();
    }
    
    private void loadPB() {
        File f=new File(fpb);
        if(f.exists())
            lpb=IOFile.doc(fpb);
        else
            lpb=new ArrayList<>();
    }
    
    private void showNV(List<NhanVien> list) {
        tmnv.setRowCount(0);
        for(NhanVien i:list)
            tmnv.addRow(i.toObject());
    }
    
    private void showPB(List<PhongBan> list) {
        tmpb.setRowCount(0);
        for(PhongBan i:list)
            tmpb.addRow(i.toObject());
    }

