    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        int n=lnv.size();
        if(n>0) 
            NhanVien.setSma(lnv.get(n-1).getMa()+1);
        jTextField1.setText(NhanVien.getSma()+"");
        String hoten, dchi, sdt;
        int bacluong;
        try{
            hoten=jTextField2.getText();
            dchi=jTextField3.getText();
            sdt=jTextField4.getText();
            bacluong=Integer.parseInt(jTextField5.getText());
            NhanVien nv= new NhanVien(bacluong, hoten, dchi, sdt);
            tmnv.addRow(nv.toObject());
            lnv.add(nv);
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "bac luong phai la so");
        } catch(SoDienThoaiException e){
            JOptionPane.showMessageDialog(this, "sdt phai la so");
        } catch(TrongException e){
            JOptionPane.showMessageDialog(this, "khong duoc de trong");
        } catch(bacLuongException e){
            JOptionPane.showMessageDialog(this, "bac luong phai trong khoang 1-9");
        }
    }//GEN-LAST:event_btThemActionPerformed

