    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex()==1){
            lql.sort(new Comparator<QLSX>() {
                @Override
                public int compare(QLSX o1, QLSX o2) {
                    String[] ten1=o1.getPhonghoc().getTenPhong().split("\\s+");
                    String[] ten2=o2.getPhonghoc().getTenPhong().split("\\s+");
                    if(ten1[ten1.length-1].equalsIgnoreCase(ten2[ten2.length-1])) {
                        return o1.getPhonghoc().getTenPhong().compareToIgnoreCase(o2.getPhonghoc().getTenPhong());
                    } else 
                        return ten1[ten1.length-1].compareToIgnoreCase(ten2[ten2.length-1]);
                }
            });
        }
        if(cbSapXep.getSelectedIndex()==2){
            lql.sort(new Comparator<QLSX>() {
                @Override
                public int compare(QLSX o1, QLSX o2) {
//                      return o1.getDichvu().getTen().compareToIgnoreCase(o2.getDichvu().getTen());
                    return Integer.compare(o1.getKip(), o2.getKip());
                }
                
            });
        }
        
        tmql.setRowCount(0);
        for(QLSX i:lql)
            tmql.addRow(i.toObject());
    }//GEN-LAST:event_cbSapXepItemStateChanged

