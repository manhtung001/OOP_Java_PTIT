    private void loadPH() {
        File f=new File(fph);
        if(f.exists()) 
            lph=IOFile.doc(fph);
        else 
            lph=new ArrayList<>();
    }
    
    private void loadLOP() {
        File f=new File(fl);
        if(f.exists()) 
            ll=IOFile.doc(fl);
        else 
            ll=new ArrayList<>();
    }
    
    private void load2Ma() {
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        
        for(PhongHoc i:lph)
            jComboBox2.addItem(i.getMa()+"");
        
        for(Lop i:ll)
            jComboBox3.addItem(i.getMa()+"");
    }
    
    private void loadQL() {
        File f=new File(fql);
        if(f.exists()) 
            lql=IOFile.doc(fql);
        else 
            lql=new ArrayList<>();
    }
    
    private void showPH(List<PhongHoc> list) {
        tmph.setRowCount(0);
        for(PhongHoc i:list)
            tmph.addRow(i.toObject());
    }
    
    private void showLOP(List<Lop> list) {
        tml.setRowCount(0);
        for(Lop i:list)
            tml.addRow(i.toObject());
    }
    
    private void showQL(List<QLSX> list) {
        tmql.setRowCount(0);
        for(QLSX i:list)
            tmql.addRow(i.toObject());
    }

