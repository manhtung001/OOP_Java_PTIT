    private PhongHoc getPHbyMa(int ma) {
        for(PhongHoc i:lph)
            if(i.getMa()==ma)
                return i;
        return null;
    }
    
    private Lop getLopbyMa(int ma) {
        for(Lop i:ll)
            if(i.getMa()==ma)
                return i;
        return null;
    }
    
    private boolean trung2Ma(int maPH, int maLop) {
        for(QLSX i:lql)
            if(i.getPhonghoc().getMa()==maPH && i.getLop().getMa()==maLop)
                return true;
        return false;
    }
    
    private void btThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThem2ActionPerformed
        try {
            int maPH=Integer.parseInt(jComboBox2.getSelectedItem().toString());
            int maLop=Integer.parseInt(jComboBox3.getSelectedItem().toString());
            if(trung2Ma(maPH, maLop)) {
                JOptionPane.showMessageDialog(this, "Trung 2 ma!");
                jComboBox2.requestFocus();
                return;
            }
            int kip=Integer.parseInt(jTextField12.getText());
            PhongHoc ph=getPHbyMa(maPH);
            Lop lop=getLopbyMa(maLop);
            if(lop.getSoSinhVien() > ph.getSoGhe()) {
                JOptionPane.showMessageDialog(this, "phong nay co so ghe it hon so sinh vien. Vui long chon phong khac");
                jComboBox2.requestFocus();
                return;
            }
            QLSX ql = new QLSX(ph, lop, kip);
            tmql.addRow(ql.toObject());
            lql.add(ql);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ma phong hoc hoac ma lop phai la so");
        }
    }//GEN-LAST:event_btThem2ActionPerformed

